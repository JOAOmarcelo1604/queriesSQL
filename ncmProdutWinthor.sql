WITH UltimoMov AS (
    SELECT
        m.CODPROD,
        m.CODFILIAL,
        m.SITTRIBUT AS CST_ICMS_MOV,
        m.PERCICM AS ALIQ_ICMS_MOV,
        m.CODSITTRIBPISCOFINS AS CST_PC_MOV,
        m.PERPIS AS PERPIS_MOV,
        m.PERCOFINS AS PERCOFINS_MOV,
        m.DTMOV,
        ROW_NUMBER() OVER (
            PARTITION BY m.CODPROD, m.CODFILIAL
            ORDER BY m.DTMOV DESC
        ) AS RN
    FROM PCMOV m
    WHERE m.CODOPER = 'S'
      AND m.DTCANCEL IS NULL
      AND m.SITTRIBUT IS NOT NULL
      AND m.DTMOV >= ADD_MONTHS(TRUNC(SYSDATE), -24)
      AND m.CODFILIAL IN ('1','2','3','4','5','6','7')
)
SELECT DISTINCT
    p.CODPROD AS COD_PRODUTO,
    p.DESCRICAO AS DESCRICAO,
    p.NBM AS NCM,
    pf.CODFILIAL AS COD_FILIAL,

    COALESCE(
        ti_tab.SITTRIBUT,
        ti_ncm.SITTRIBUT,
        um.CST_ICMS_MOV,
        p.SITTRIBUT
    ) AS CST_ICMS,

    COALESCE(
        CASE WHEN fil.UF = 'BA' THEN ti_tab.ALIQICMS1 ELSE ti_tab.ALIQICMS2 END,
        CASE WHEN fil.UF = 'BA' THEN ti_ncm.ALIQICMS1 ELSE ti_ncm.ALIQICMS2 END,
        um.ALIQ_ICMS_MOV,
        p.PERICM
    ) AS ALIQUOTA_ICMS,

    COALESCE(
        pc_tab.SITTRIBUT,
        pc_ncm.SITTRIBUT,
        um.CST_PC_MOV,
        pc_fil.SITTRIBUT
    ) AS CST_PIS_COFINS,

    COALESCE(
        pc_tab.PERCPIS,
        pc_ncm.PERCPIS,
        um.PERPIS_MOV,
        pc_fil.PERCPIS,
        pf.PERPIS
    ) AS ALIQUOTA_PIS,

    COALESCE(
        pc_tab.PERCCOFINS,
        pc_ncm.PERCCOFINS,
        um.PERCOFINS_MOV,
        pc_fil.PERCCOFINS,
        pf.PERCOFINS
    ) AS ALIQUOTA_COFINS,

    CASE
        WHEN ti_tab.CODST IS NOT NULL THEN 'PCTABTRIB (cadastro)'
        WHEN ti_ncm.CODST IS NOT NULL THEN 'PCTRIBVENDAUF (NCM)'
        WHEN um.CST_ICMS_MOV IS NOT NULL THEN 'PCMOV (NF ' || TO_CHAR(um.DTMOV,'DD/MM/YYYY') || ')'
        WHEN p.SITTRIBUT IS NOT NULL THEN 'PCPRODUT (default)'
        ELSE 'SEM DADO'
    END AS ORIGEM_ICMS,

    CASE
        WHEN pc_tab.CODTRIBPISCOFINS IS NOT NULL THEN 'PCTABTRIB (cadastro)'
        WHEN pc_ncm.CODTRIBPISCOFINS IS NOT NULL THEN 'PCTRIBVENDAUF (NCM)'
        WHEN um.CST_PC_MOV IS NOT NULL THEN 'PCMOV (NF ' || TO_CHAR(um.DTMOV,'DD/MM/YYYY') || ')'
        WHEN pc_fil.CODTRIBPISCOFINS IS NOT NULL THEN 'PCPRODFILIAL (default)'
        ELSE 'SEM DADO'
    END AS ORIGEM_PISCOFINS

FROM PCPRODUT p

INNER JOIN PCPRODFILIAL pf
    ON pf.CODPROD = p.CODPROD
   AND pf.CODFILIAL IN ('6')

LEFT JOIN PCFILIAL fil
    ON fil.CODIGO = pf.CODFILIAL

LEFT JOIN PCTABTRIB tt
    ON tt.CODPROD = p.CODPROD
   AND tt.CODFILIALNF = pf.CODFILIAL
   AND tt.UFDESTINO = 'BA'

LEFT JOIN PCTRIBUT ti_tab
    ON ti_tab.CODST = tt.CODST

LEFT JOIN PCTRIBPISCOFINS pc_tab
    ON pc_tab.CODTRIBPISCOFINS = tt.CODTRIBPISCOFINS

LEFT JOIN PCTRIBVENDAUF tr_uf
    ON tr_uf.CODNCMEX = p.NBM
   AND tr_uf.CODFILIAL = pf.CODFILIAL
   AND tr_uf.UFDESTINO = 'BA'

LEFT JOIN PCTRIBUT ti_ncm
    ON ti_ncm.CODST = tr_uf.CODST

LEFT JOIN PCTRIBPISCOFINS pc_ncm
    ON pc_ncm.CODTRIBPISCOFINS = tr_uf.CODTRIBPISCOFINS

LEFT JOIN UltimoMov um
    ON um.CODPROD = p.CODPROD
   AND um.CODFILIAL = pf.CODFILIAL
   AND um.RN = 1

LEFT JOIN PCTRIBPISCOFINS pc_fil
    ON pc_fil.CODTRIBPISCOFINS = pf.CODSITTRIBPISCOFINS

